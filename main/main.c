static esp_err_t epd_send(uint8_t dc, const uint8_t *data, size_t len)
{
    gpio_set_level(PIN_NUM_DC, dc);
    spi_transaction_t t = {
        .length = len * 8,
        .tx_buffer = data,
    };
    return spi_device_transmit(epd_spi, &t);
}

static inline esp_err_t epd_cmd(uint8_t cmd)
{
    return epd_send(0, &cmd, 1);
}

static inline esp_err_t epd_data(const uint8_t *data, size_t len)
{
    return epd_send(1, data, len);
}

static void epd_wait_busy(void)
{
    // De flesta 2.13"-moduler signalerar BUSY=1 under arbete.
    int timeout_ms = 7000;
    while (gpio_get_level(PIN_NUM_BUSY) == 1 && timeout_ms > 0) {
        vTaskDelay(pdMS_TO_TICKS(20));
        timeout_ms -= 20;
    }
    if (timeout_ms <= 0) {
        ESP_LOGW(TAG, "BUSY timeout, continuing anyway");
    }
}

static void epd_hw_reset(void)
{
    gpio_set_level(PIN_NUM_RST, 0);
    vTaskDelay(pdMS_TO_TICKS(20));
    gpio_set_level(PIN_NUM_RST, 1);
    vTaskDelay(pdMS_TO_TICKS(20));
}

static esp_err_t epd_set_window_full(void)
{
    uint8_t x_range[2] = {0x00, EPD_RAW_WIDTH_BYTES - 1};
    uint8_t y_range[4] = {0x00, 0x00, (EPD_RAW_HEIGHT - 1) & 0xFF, (EPD_RAW_HEIGHT - 1) >> 8};

    ESP_ERROR_CHECK(epd_cmd(0x44)); // SET_RAM_X_ADDR_START_END_POSITION
    ESP_ERROR_CHECK(epd_data(x_range, sizeof(x_range)));

    ESP_ERROR_CHECK(epd_cmd(0x45)); // SET_RAM_Y_ADDR_START_END_POSITION
    ESP_ERROR_CHECK(epd_data(y_range, sizeof(y_range)));

    uint8_t x = 0x00;
    uint8_t y[2] = {0x00, 0x00};
    ESP_ERROR_CHECK(epd_cmd(0x4E)); // SET_RAM_X_ADDR_COUNTER
    ESP_ERROR_CHECK(epd_data(&x, 1));
    ESP_ERROR_CHECK(epd_cmd(0x4F)); // SET_RAM_Y_ADDR_COUNTER
    ESP_ERROR_CHECK(epd_data(y, 2));
    return ESP_OK;
}

static esp_err_t epd_init(void)
{
    spi_bus_config_t buscfg = {
        .mosi_io_num = PIN_NUM_MOSI,
        .miso_io_num = -1,
        .sclk_io_num = PIN_NUM_CLK,
        .quadwp_io_num = -1,
        .quadhd_io_num = -1,
        .max_transfer_sz = EPD_BUF_SIZE + 16,
    };

    spi_device_interface_config_t devcfg = {
        .clock_speed_hz = EPD_SPI_HZ,
        .mode = 0,
        .spics_io_num = PIN_NUM_CS,
        .queue_size = 1,
    };

    gpio_config_t io_conf = {
        .pin_bit_mask = (1ULL << PIN_NUM_DC) | (1ULL << PIN_NUM_RST),
        .mode = GPIO_MODE_OUTPUT,
    };
    ESP_ERROR_CHECK(gpio_config(&io_conf));

    gpio_config_t busy_conf = {
        .pin_bit_mask = (1ULL << PIN_NUM_BUSY),
        .mode = GPIO_MODE_INPUT,
        .pull_up_en = GPIO_PULLUP_ENABLE,
    };
    ESP_ERROR_CHECK(gpio_config(&busy_conf));

    ESP_ERROR_CHECK(spi_bus_initialize(EPD_SPI_HOST, &buscfg, SPI_DMA_CH_AUTO));
    ESP_ERROR_CHECK(spi_bus_add_device(EPD_SPI_HOST, &devcfg, &epd_spi));

    epd_hw_reset();
    epd_wait_busy();

    uint8_t tmp[3];

    ESP_ERROR_CHECK(epd_cmd(0x12)); // SWRESET
    epd_wait_busy();

    // Driver output control (gate lines = 250)
    tmp[0] = (EPD_RAW_HEIGHT - 1) & 0xFF;
    tmp[1] = (EPD_RAW_HEIGHT - 1) >> 8;
    tmp[2] = 0x00;
    ESP_ERROR_CHECK(epd_cmd(0x01));
    ESP_ERROR_CHECK(epd_data(tmp, 3));

    tmp[0] = 0x03; // X+, Y+ auto-increment
    ESP_ERROR_CHECK(epd_cmd(0x11));
    ESP_ERROR_CHECK(epd_data(tmp, 1));

    ESP_ERROR_CHECK(epd_set_window_full());

    tmp[0] = 0x05; // border waveform
    ESP_ERROR_CHECK(epd_cmd(0x3C));
    ESP_ERROR_CHECK(epd_data(tmp, 1));

    tmp[0] = 0x80; // internal temp sensor
    ESP_ERROR_CHECK(epd_cmd(0x18));
    ESP_ERROR_CHECK(epd_data(tmp, 1));

    return ESP_OK;
}

static void epd_clear_buffer(uint8_t color_white)
{
    memset(epd_fb, color_white ? 0xFF : 0x00, sizeof(epd_fb));
}

static void epd_draw_pixel(int x, int y, uint8_t black)
{
    if (x < 0 || x >= SCREEN_WIDTH || y < 0 || y >= SCREEN_HEIGHT) {
        return;
    }
    // Map to panel RAM and flip so text is upright in landscape.
    int xr = (SCREEN_HEIGHT - 1) - y;
    int yr = x;

    size_t idx = (yr * EPD_RAW_WIDTH_BYTES) + (xr / 8);
    uint8_t bit = 0x80 >> (xr & 7);
    if (black) {
        epd_fb[idx] &= ~bit;
    } else {
        epd_fb[idx] |= bit;
    }
}

static void epd_draw_char_scaled(int x, int y, char c, int scale)
{
    if (c < 32 || c > 127) {
        c = '?';
    }
    const uint8_t *glyph = font5x7[(int)c - 32];

    for (int col = 0; col < 5; col++) {
        uint8_t line = glyph[col];
        for (int row = 0; row < 7; row++) {
            uint8_t px_on = (line >> row) & 0x01;
            if (px_on) {
                for (int dx = 0; dx < scale; dx++) {
                    for (int dy = 0; dy < scale; dy++) {
                        epd_draw_pixel(x + col * scale + dx, y + row * scale + dy, 1);
                    }
                }
            }
        }
    }
}

static void epd_draw_text_scaled(int x, int y, const char *text, int scale)
{
    int char_w = 6 * scale;
    int line_h = 10 * scale;

    while (*text) {
        epd_draw_char_scaled(x, y, *text++, scale);
        x += char_w;
        if (x > (SCREEN_WIDTH - char_w)) {
            x = 0;
            y += line_h;
        }
        if (y > (SCREEN_HEIGHT - (8 * scale))) {
            break;
        }
    }
}

static esp_err_t epd_update_full(void)
{
    ESP_ERROR_CHECK(epd_set_window_full());

    ESP_ERROR_CHECK(epd_cmd(0x24)); // WRITE_RAM (new image)
    ESP_ERROR_CHECK(epd_data(epd_fb, sizeof(epd_fb)));

    ESP_ERROR_CHECK(epd_set_window_full());
    ESP_ERROR_CHECK(epd_cmd(0x26)); // WRITE_RAM_RED/old image buffer on BW panel
    ESP_ERROR_CHECK(epd_data(epd_fb, sizeof(epd_fb)));

    uint8_t update = 0xF7; // full update sequence
    ESP_ERROR_CHECK(epd_cmd(0x22));
    ESP_ERROR_CHECK(epd_data(&update, 1));
    ESP_ERROR_CHECK(epd_cmd(0x20)); // MASTER_ACTIVATION
    epd_wait_busy();
    return ESP_OK;
}

void app_main(void)
{
    ESP_LOGI(TAG, "Initializing e-paper");
    ESP_ERROR_CHECK(epd_init());

    epd_clear_buffer(1);
    epd_draw_text_scaled(8, 10, "ESP32-S3", TEXT_SCALE);
    epd_draw_text_scaled(8, 46, "WeAct 2.13", TEXT_SCALE);
    epd_draw_text_scaled(8, 82, "Hello world!", TEXT_SCALE);

    ESP_LOGI(TAG, "Updating display");
    ESP_ERROR_CHECK(epd_update_full());
    ESP_LOGI(TAG, "Done");
}
